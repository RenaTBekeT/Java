package ExpressionsAndWorkFlow.src;

public class Swap {
    public static void main(String[] args) {
        int a = 123;
        int b = 526;

        System.out.println(a);
        System.out.println(b);

        System.out.println("Swapped values of x and y");
        System.out.println("a = " + b);
        System.out.println("b = " + a);
    }
}
