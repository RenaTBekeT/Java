package ExpressionsAndWorkFlow.src;

public class TwoNumbers {
    public static void main(String[] args) {
        double a = 13;
        double b = 22;

        System.out.println(a + b);
        System.out.println(b - a);
        System.out.println(a * b);
        System.out.println(b / a);
        System.out.println((int) (b/a));
        System.out.println(b % a);
    }
}
